package pack17112025;

public class Pro18 {
	public static void main(String args[])
	{
		int a = 10;
		int b = 20;
		
		if(a<b)
		{
			if(a==b)
			{
				System.out.println( + a+ " is equal to " + b);
			}
			else
			{
				System.out.println( +a + "less than " + b);
			}
		}
		else
		{
			System.out.println( +b + "is greater than " +a );
		}
	}
}
