//program to declare, initialize, and print different data types in Java
package basicPrograms;

public class Data_types_in_Java {

	public static void main(String[] args) {
		byte b = 20;
		short s = 1200;
		int i = 20765;
		long l = 209876;
		
		boolean	 b1 = true;
		
		double d = 24578.40;
		float f = 67.78f;
		
		char c = 'S';	
		String s1 = "sneha";
		
		System.out.println("Byte datatype is " +b);
		System.out.println("Short datatype is " +s);
		System.out.println("Int datatype is " +i);
		System.out.println("Long datatype is " +l);
		
		System.out.println("Boolean datatype is " +b1);
		System.out.println("Double datatype is "+d);
		System.out.println("Float datatype is " +f);
		
		System.out.println("Char datatype is " +c);
		System.out.println("String datatype " +s1);
		
	}
}
