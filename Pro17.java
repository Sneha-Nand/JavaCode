package pack17112025;

public class Pro17 {

	public static void main(String args[])
	{
		int a = 11;
		int b = 10;
		
		if(a>b)
		{
			System.out.println(+ a + " is greater than " + b);
		}
		else if(a==b)
		{
			System.out.println(+ a + " and " + b+ " both are equal");
		}
		else
		{
			System.out.println(+ a + "is smaller than " + b);
		}
	}
}
